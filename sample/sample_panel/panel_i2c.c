const CVI_U32 panel_addr_byte = 1;
const CVI_U32 panel_data_byte = 1;
static int g_fd[VO_MAX_DEV_NUM] = {[0 ... (VO_MAX_DEV_NUM - 1)] = -1};

PANEL_COMMBUS_U g_aunPanel_BusInfo[VO_MAX_DEV_NUM] = {
	[0] = { .s8I2cDev = 1},
};

PANEL_COMMADDR_U g_aunPanel_AddrInfo[VO_MAX_DEV_NUM] = {
	[0] = { .s8I2cAddr = 0x42},
};

int panel_i2c_init(VO_DEV VoDev)
{
	char acDevFile[16] = {0};
	CVI_U8 u8DevNum;

	if (g_fd[VoDev] >= 0)
		return CVI_SUCCESS;

	u8DevNum = g_aunPanel_BusInfo[VoDev].s8I2cDev;
	snprintf(acDevFile, sizeof(acDevFile),  "/dev/i2c-%u", u8DevNum);

	g_fd[VoDev] = open(acDevFile, O_RDWR);

	if (g_fd[VoDev] < 0) {
		printf("Open /dev/i2c-%u error!\n", u8DevNum);
		return CVI_FAILURE;
	}
	return CVI_SUCCESS;
}

int panel_i2c_exit(VO_DEV VoDev)
{
	if (g_fd[VoDev] >= 0) {
		close(g_fd[VoDev]);
		g_fd[VoDev] = -1;
		return CVI_SUCCESS;
	}
	return CVI_FAILURE;
}

int panel_write_register(VO_DEV VoDev, int addr, int data)
{
	CVI_U8 idx = 0;
	int ret;
	CVI_U8 buf[8];
	struct i2c_rdwr_ioctl_data packets;
	struct i2c_msg messages[2];

	messages[0].addr = g_aunPanel_AddrInfo->s8I2cAddr;
	messages[0].len = panel_addr_byte + panel_data_byte;
	messages[0].flags = 0;

	if (panel_addr_byte == 2) {
		buf[idx] = (addr >> 8) & 0xff;
		idx++;
		buf[idx] = addr & 0xff;
		idx++;
	}else if (panel_addr_byte == 1){
		buf[idx] = addr & 0xff;
		idx++;
	}

	if (panel_data_byte == 1) {
		buf[idx] = data & 0xff;
	}

	messages[0].buf = buf;
	/* Send the request to the kernel and get the result back */
	packets.msgs = messages;
	packets.nmsgs = 1;
	ret = ioctl(g_fd[VoDev], I2C_RDWR, &packets);
	if (ret < 0) {
		printf("I2C_WRITE error!\n");
		return CVI_FAILURE;
	};

	return CVI_SUCCESS;
}

